#双引号   "      "
#三单引号'''    '''
#三双引号"""    """

print('Hello!')
#Hello!
print("Hello!")
#Hello!
print('''Hello!''')
#Hello!
print('''Hello
         world
         !''')
#Hello!
#         world
#         !
print("""Hello '''!""")
#Hello '''!

