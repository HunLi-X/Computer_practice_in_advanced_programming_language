#2.6.3  比较运算符（关系运算符）6个：
#  ==等于  !=不等于  >大于   <小于   >=大于或等于    <=小于或等于
#  结果只有2个：True   False
a=10
b=20
print(a==b)
#False
print(a!=b)
#True
print(a>b)
#False
print(a<b)
#True
print(a>=b)
#False
print(a<=b)
#True
print(a+10>=b)
#True
print(a+10>b)
#False
print(a<=b-10)
#True
print(a<b-10)
#False
print(a==b-10)
#True
