#2.6.5  位运算符5个：
# &按位与  |按位或  ^按位异或  ~按位取反  <<按位左移   >>按位右移
a=60   #a=60=32+16+8+4+0+0=0011_1100b
b=13   #b=13=      8+4+0+1=0000_1101b
c=0
c=a&b  #c=a&b=0011_1100b & 0000_1101b=0000_1100b=12
print(c)
#12
c=a|b  #c=a|b=0011_1100b | 0000_1101b=0011_1101b=32+16+8+4+0+1=61
print(c)
#61
c=a^b  #c=a^b=0011_1100b ^ 0000_1101b=0011_0001b=32+16+0+0+0+1=49
print(c)
#49
c=~a   #c=~a=~0011_1100b=1100_0011b补码=1100_0010b反码=1011_1101b原码
########1011_1101b原码=-011_1101b真值=-(32+16+8+4+0+1)真值=-61真值
print(c)
#-61
c=a<<2 #a=0011_1100b左移2bit之后a=1111_0000b=128+64+32+16_+0+0+0+0=240
print(c)
#240
c=a>>2 #a=0011_1100b右移2bit之后a=0000_1111b=8+4+2+1=15
print(c)
#15

